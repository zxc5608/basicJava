package E_oop;

public class Calculator {

	
	double plus(double a,double b){
		return a+b;
	}

	double gop(double c,double d){
		return c*d;
		
	}
	double na(double aa,double bb){
		return aa/bb;
		
	}
	double mius(double cc, double dd){
		return cc-dd;
	}
	double nn(double ss,double qq){
		return ss % qq;
	}
}
